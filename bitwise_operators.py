# Bitwise Operators Program

# Input numbers
a = int(input("Enter first number (a): "))
b = int(input("Enter second number (b): "))

print("\n=== BITWISE OPERATIONS ===")
print("a :",a)
print("b :",b)

# AND Operator
print("AND Operator :", a & b)

print("OR Operator :", a | b)
print("NOT Operator ~a:", ~a)
print("NOT Operator ~b:", ~b)
print("XOR Operator :", a ^ b)
print("left shift Operator :", a << b)
print("right shift Operator :", a >> b)
